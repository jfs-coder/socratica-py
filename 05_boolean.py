# Boolean values: True, False
a = 3
b = 5
print('a = 3   b = 5')
print(f'a == b: {a == b}') # prints False
print(f'a != b: {a != b}') # prints True
print(f'a > b: {a > b}') # prints False
print(f'a < b: {a < b}') # prints True
print(f'type (True): {type(True)}') # prints 'bool'
print(f'type (False): {type(False)}') # prints 'bool'
print(f'bool(28): {bool(28)}') # prints True
print(f'bool(-2.71828): {bool(-2.71828)}') # prints True
print(f'bool(0): {bool(0)}') # prints False
print(f'bool("Turing"): {bool("Turing")}') # prints True
print(f'bool(" "): {bool(" ")}') # prints True
print(f'bool(""): {bool("")}') # prints False

